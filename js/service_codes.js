function Filter(input) {
    let value = input.value;
    let td = input.parentNode;
    let tr = td.parentNode;
    let table = tr.parentNode;
    let i = 0;
    let values = new Set();
    for (let row of table.rows) {
        if (i == 1) {
            irow = row;
        }
        row.style.display = '';
        if (i > 1) {
            let j = 0;
            for (let cell of irow.cells) {
                let value = cell.firstChild.value;
                let text = '';
                if (row.cells[j]) {
                    text = row.cells[j].innerHTML;
                } else {
                    continue;
                }
                if (
                    value != '#' &&
                    text.toLowerCase().indexOf(value.toLowerCase()) < 0
                ) {
                    row.style.display = 'none';
                }
                j++;
            }
            if (row.style.display == '') {
                let j = 0;
                for (let cell of irow.cells) {
                    let value = cell.firstChild.value;
                    let text = '';
                    if (row.cells[j]) {
                        text = row.cells[j].innerHTML;
                    } else {
                        continue;
                    }
                    if (value == '#') {
                        values.add("'" + text + "'");
                    }
                    j++;
                }
            }
        }
        i++;
    }
    let pre = document.createElement('pre');
    pre.innerHTML = Array.from(values).sort().join(', ');
    document.body.appendChild(pre);
}

